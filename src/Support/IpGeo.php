    /**
     * 解析 IP → 国家代码 + 名称 + 经纬度
     * @param string $ip
     * @return array [country, name, lat, lng]
     */
    public static function lookup($ip) {
        self::initRanges();

        // 检查本地/私有 IP
        if (self::isPrivateIp($ip)) {
            return self::formatResult('LOCAL');
        }

        $ipLong = (int)sprintf("%u", ip2long($ip));

        // 二分查找
        $left = 0;
        $right = count(self::$ipRanges) - 1;
        $country = 'UNKNOWN';

        // 由于范围有重叠（同一个 /8 段可能对应多个国家），
        // 我们用线性搜索找到第一个匹配，优先匹配后面（更具体）的条目
        // 但为了性能，先用第一 octet 快速过滤
        $firstOctet = (int)explode('.', $ip)[0];

        // 简化：线性搜索（范围数量有限，性能足够）
        foreach (self::$ipRanges as $range) {
            if ($ipLong >= $range[0] && $ipLong <= $range[1]) {
                $country = $range[2];
            }
        }

        return self::formatResult($country);
    }

    /**
     * 批量解析 IP（用于优化性能）
     */
    public static function batchLookup($ips) {
        $results = [];
        $cache = [];
        foreach ($ips as $ip) {
            if (!isset($cache[$ip])) {
                $cache[$ip] = self::lookup($ip);
            }
            $results[$ip] = $cache[$ip];
        }
        return $results;
    }

    /**
     * 判断是否为私有/本地 IP
     */
    private static function isPrivateIp($ip) {
        $long = (int)sprintf("%u", ip2long($ip));
        $localStart = (int)sprintf("%u", ip2long('127.0.0.0'));
        $localEnd = (int)sprintf("%u", ip2long('127.255.255.255'));
        $priv10Start = (int)sprintf("%u", ip2long('10.0.0.0'));
        $priv10End = (int)sprintf("%u", ip2long('10.255.255.255'));
        $priv172Start = (int)sprintf("%u", ip2long('172.16.0.0'));
        $priv172End = (int)sprintf("%u", ip2long('172.31.255.255'));
        $priv192Start = (int)sprintf("%u", ip2long('192.168.0.0'));
        $priv192End = (int)sprintf("%u", ip2long('192.168.255.255'));

        return ($long >= $localStart && $long <= $localEnd)
            || ($long >= $priv10Start && $long <= $priv10End)
            || ($long >= $priv172Start && $long <= $priv172End)
            || ($long >= $priv192Start && $long <= $priv192End);
    }

    /**
     * 格式化返回结果
     */
    private static function formatResult($countryCode) {
        $info = self::$countryCoords[$countryCode] ?? self::$countryCoords['UNKNOWN'];
        return [
            'country' => $countryCode,
            'name' => $info['name'],
            'lat' => $info['lat'],
            'lng' => $info['lng'],
        ];
    }

    /**
     * 获取所有国家列表（用于前端筛选）
     */
    public static function getCountryList() {
        $list = [];
        foreach (self::$countryCoords as $code => $info) {
            $list[] = ['code' => $code, 'name' => $info['name']];
        }
        return $list;
    }
